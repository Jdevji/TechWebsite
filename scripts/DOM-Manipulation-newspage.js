//DOM manipulation for colour change of buttons on the news article page

    function changeCbtn1(){//when the button with id open-article1 is hoverd over this function is run and its background style property is set to blue.
        document.getElementById("open-article1").style.background = "blue";
    }
    
    function backtoNbtn1(){// when the mouse moves of the button with id open-article1 the background style property of it is set back to normal.
        document.getElementById("open-article1").style.background = "#e8491d";
    }  
   


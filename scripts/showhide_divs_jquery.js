$(document).ready(function(){//when the document is ready...
$('#open-article1').click(function()//when button with id open_article1 is clicked.
    {
     $('#article-1').show('blind');// the div with id article_1 is shown with a blind annimation effect.
    
    });

$('#close-article1').click(function()//when button with id close_article1 is clicked.
    {
     $('#article-1').hide('blind');//the div with id article_1 is closed with a blind annimation effect.
    
    });

});


